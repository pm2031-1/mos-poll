#include <iostream>
#include <vector>
#include <algorithm>
#include <queue>

using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n;
    cin >> n;

    vector<pair<long long, long long>> boxes(n); // (w, c)
    for (int i = 0; i < n; i++) {
        cin >> boxes[i].first >> boxes[i].second;
    }

    // Сортируем по грузоподъёмности c (маленькие сначала)
    sort(boxes.begin(), boxes.end(), [](const auto& A, const auto& B) {
        if (A.second != B.second) return A.second < B.second;
        return A.first < B.first; // если c одинаковые, легче раньше (не обязательно, но логично)
        });

    priority_queue<long long> pq; // храним веса выбранных коробок, сверху самый тяжёлый
    long long W = 0;              // общий вес текущей стопки (вес всех выбранных коробок)

    for (auto [w, c] : boxes) {

        // Если текущий вес стопки сверху (W) <= c, то эта коробка может быть снизу
        if (W <= c) {
            pq.push(w);  // добавляем коробку в стопку
            W += w;      // общий вес стопки вырос
        }
        else {
            // Иначе коробка не выдерживает текущую стопку сверху.
            // Но можно попробовать заменить самую тяжёлую коробку на более лёгкую
            // чтобы уменьшить W и облегчить будущие добавления.
            if (!pq.empty() && pq.top() > w) {
                W -= pq.top(); // убрали самую тяжёлую
                pq.pop();

                pq.push(w);    // поставили эту (она легче)
                W += w;        // обновили общий вес
            }
            // если заменить нельзя — просто пропускаем коробку
        }
    }

    // Количество коробок в pq = максимальная высота стопки
    cout << (int)pq.size() << "\n";
    return 0;
}

